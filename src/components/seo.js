/**
 * SEO component that queries for data with
 *  Gatsby's useStaticQuery React hook
 *
 * See: https://www.gatsbyjs.org/docs/use-static-query/
 */

import React from "react"
import PropTypes from "prop-types"
import Helmet from "react-helmet"

function SEO({ title, lang }) {
  const metaDescription =
    "Jelena Viskovic is an artist and software developer. Her work combines sculpture and simulation. She uses video game engines to respond to emerging and archaic technologies, social organisational platforms, and public spaces. Her projects have been commissioned by the V&A, Rhizome, Transmediale, The New Institute. Viskovic is a visiting lecturer at the Royal College of Art and the AA School of Architecture and currently lives in the UK."
  title = title ? `${title} | Jelena Viskovic` : "Jelena Viskovic";
  return (
    <Helmet
      htmlAttributes={{
        lang,
      }}
      title={title}
      meta={[
        {
          name: `description`,
          content: metaDescription,
        },
        {
          property: `og:title`,
          content: title,
        },
        {
          property: `og:description`,
          content: metaDescription,
        },
        {
          property: `og:type`,
          content: `website`,
        },
        {
          name: `twitter:card`,
          content: `summary`,
        },
        {
          name: `twitter:title`,
          content: title,
        },
        {
          name: `twitter:description`,
          content: metaDescription,
        },
      ]}
    />
  )
}

SEO.defaultProps = {
  lang: `en`,
  meta: [],
  description: ``,
}

SEO.propTypes = {
  description: PropTypes.string,
  lang: PropTypes.string,
  meta: PropTypes.arrayOf(PropTypes.object),
  title: PropTypes.string,
}

export default SEO
